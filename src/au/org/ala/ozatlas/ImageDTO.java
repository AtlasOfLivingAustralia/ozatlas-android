package au.org.ala.ozatlas;

public class ImageDTO {

	public String infoSourceUid;
	public String infoSourceName;
	public String infoSourceURL;
	public String title;
	public String identifier;
	public String contentType;
	public String creator;
	public String isPartOf;
	public String licence;
	public String occurrenceUid;
	public String thumbnail;
	public String largeImageUrl;
	public String smallImageUrl;
	
	public String getInfoSourceUid() {
		return infoSourceUid;
	}
	public void setInfoSourceUid(String infoSourceUid) {
		this.infoSourceUid = infoSourceUid;
	}
	public String getInfoSourceName() {
		return infoSourceName;
	}
	public void setInfoSourceName(String infoSourceName) {
		this.infoSourceName = infoSourceName;
	}
	public String getInfoSourceURL() {
		return infoSourceURL;
	}
	public void setInfoSourceURL(String infoSourceURL) {
		this.infoSourceURL = infoSourceURL;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getIdentifier() {
		return identifier;
	}
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	public String getContentType() {
		return contentType;
	}
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getIsPartOf() {
		return isPartOf;
	}
	public void setIsPartOf(String isPartOf) {
		this.isPartOf = isPartOf;
	}
	public String getLicence() {
		return licence;
	}
	public void setLicence(String licence) {
		this.licence = licence;
	}
	public String getOccurrenceUid() {
		return occurrenceUid;
	}
	public void setOccurrenceUid(String occurrenceUid) {
		this.occurrenceUid = occurrenceUid;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getLargeImageUrl() {
		return largeImageUrl;
	}
	public void setLargeImageUrl(String largeImageUrl) {
		this.largeImageUrl = largeImageUrl;
	}
	public String getSmallImageUrl() {
		return smallImageUrl;
	}
	public void setSmallImageUrl(String smallImageUrl) {
		this.smallImageUrl = smallImageUrl;
	}
}
